export {}; // モジュール化をすることで、変数のダブりを回避する

//////////////////////////////////////////////////////////
// 値を返さない関数
//////////////////////////////////////////////////////////
function logger(message: string): void {
  console.log(message); //→Hello World!
}
logger("Hello World!");

//////////////////////////////////////////////////////////
// 終了しない関数
//////////////////////////////////////////////////////////
function error(message: string): never {
  throw new Error(message);
}
error("Error!");
