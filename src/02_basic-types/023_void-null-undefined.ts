export {}; // モジュール化をすることで、変数のダブりを回避する

//////////////////////////////////////////////////////////
// void型
//////////////////////////////////////////////////////////
function logger(message: string): void {
  console.log(message); //→Hello World!
}
logger("Hello World!");

//////////////////////////////////////////////////////////
// null型
//////////////////////////////////////////////////////////
let n: null = null;
console.log(n); //→null

//////////////////////////////////////////////////////////
// undefined型
//////////////////////////////////////////////////////////
let u: undefined = undefined;
console.log(u); //→undefined
